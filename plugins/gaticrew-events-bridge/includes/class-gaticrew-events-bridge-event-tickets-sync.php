	}

	/**
	 * Returns the bridge booking ID stored on the order.
	 *
	 * @param WC_Order $order Woo order.
	 * @return string
	 */
	private static function get_order_booking_id( WC_Order $order ) {
		$booking_id = GatiCrew_Events_Bridge_Bookings::sanitize_booking_id( $order->get_meta( self::get_order_meta_key( 'ORDER_META_BOOKING_ID', '_gaticrew_booking_id' ), true ) );

		if ( '' !== $booking_id ) {
			return $booking_id;
		}

		foreach ( $order->get_items( 'line_item' ) as $item ) {
			if ( $item instanceof WC_Order_Item_Product ) {
				$booking_id = GatiCrew_Events_Bridge_Bookings::sanitize_booking_id( $item->get_meta( '_gaticrew_booking_id', true ) );

				if ( '' !== $booking_id ) {
					return $booking_id;
				}
			}
		}

		return '';
	}

	/**
	 * Returns the event ID stored on the order or inferred from linked product.
	 *
	 * @param WC_Order $order Woo order.
	 * @return int
	 */
	private static function get_order_event_id( WC_Order $order ) {
		$event_id = absint( $order->get_meta( self::get_order_meta_key( 'ORDER_META_EVENT_ID', '_gaticrew_event_id' ), true ) );

		if ( $event_id ) {
			return $event_id;
		}

		foreach ( $order->get_items( 'line_item' ) as $item ) {
			if ( ! $item instanceof WC_Order_Item_Product ) {
				continue;
			}

			$product_id = self::get_item_product_id( $item );
			$event_id   = self::get_event_id_by_product_id( $product_id );

			if ( $event_id ) {
				return $event_id;
			}
		}

		return 0;
	}

	/**
	 * Returns the linked Woo product ID for the order's event.
	 *
	 * @param WC_Order $order Woo order.
	 * @param int      $event_id Event ID.
	 * @return int
	 */
	private static function get_order_product_id( WC_Order $order, $event_id ) {
		foreach ( $order->get_items( 'line_item' ) as $item ) {
			if ( ! $item instanceof WC_Order_Item_Product ) {
				continue;
			}

			$product_id      = self::get_item_product_id( $item );
			$linked_event_id = self::get_event_id_by_product_id( $product_id );

			if ( $linked_event_id && absint( $event_id ) === $linked_event_id ) {
				return $product_id;
			}
		}

		return absint( get_post_meta( absint( $event_id ), self::get_event_product_meta_key(), true ) );
	}

	/**
	 * Returns total quantity for linked event items on the order.
	 *
	 * @param WC_Order $order Woo order.
	 * @param int      $event_id Event ID.
	 * @return int
	 */
	private static function get_order_quantity( WC_Order $order, $event_id ) {
		$quantity = absint( $order->get_meta( '_gaticrew_quantity', true ) );

		if ( $quantity ) {
			return $quantity;
		}

		$total = 0;

		foreach ( $order->get_items( 'line_item' ) as $item ) {
			if ( ! $item instanceof WC_Order_Item_Product ) {
				continue;
			}

			$product_id      = self::get_item_product_id( $item );
			$linked_event_id = self::get_event_id_by_product_id( $product_id );

			if ( absint( $event_id ) === $linked_event_id ) {
				$total += absint( $item->get_quantity() );
			}
		}

		return $total;
	}

	/**
	 * Returns attendee names saved on order or line item meta.
	 *
	 * @param WC_Order $order Woo order.
	 * @param int      $quantity Ticket quantity.
	 * @param string   $fallback_name Fallback customer name.
	 * @return array
	 */
	private static function get_order_attendee_names( WC_Order $order, $quantity, $fallback_name ) {
		$names = self::normalize_names_value( $order->get_meta( self::get_order_meta_key( 'ORDER_META_ATTENDEE_NAMES', '_gaticrew_attendee_names' ), true ) );

		if ( empty( $names ) ) {
			foreach ( $order->get_items( 'line_item' ) as $item ) {
				if ( ! $item instanceof WC_Order_Item_Product ) {
					continue;
				}

				$names = self::normalize_names_value( $item->get_meta( '_gaticrew_attendee_names', true ) );

				if ( ! empty( $names ) ) {
					break;
				}
			}
		}

		while ( count( $names ) < absint( $quantity ) ) {
			$names[] = sanitize_text_field( $fallback_name );
		}

		return array_slice( array_values( $names ), 0, absint( $quantity ) );
	}

	/**
	 * Normalizes stored attendee name values.
	 *
	 * @param mixed $value Raw order meta.
	 * @return array
	 */
	private static function normalize_names_value( $value ) {
		$value = maybe_unserialize( $value );

		if ( is_string( $value ) ) {
			$decoded = json_decode( $value, true );
			$value   = is_array( $decoded ) ? $decoded : array( $value );
		}

		if ( ! is_array( $value ) ) {
			return array();
		}

		$names = array();

		foreach ( $value as $name ) {
			$name = sanitize_text_field( $name );

			if ( '' !== $name ) {
				$names[] = $name;
			}
		}

		return array_values( $names );
	}

	/**
	 * Returns customer details from order billing/meta.
	 *
	 * @param WC_Order $order Woo order.
	 * @return array
	 */
	private static function get_customer_data( WC_Order $order ) {
		$name = trim( $order->get_formatted_billing_full_name() );

		if ( '' === $name ) {
			$name = sanitize_text_field( (string) $order->get_meta( self::get_order_meta_key( 'ORDER_META_CUSTOMER_NAME', '_gaticrew_customer_name' ), true ) );
		}

		$email = $order->get_billing_email();

		if ( '' === $email ) {
			$email = $order->get_meta( self::get_order_meta_key( 'ORDER_META_CUSTOMER_EMAIL', '_gaticrew_customer_email' ), true );
		}

		$phone = $order->get_billing_phone();

		if ( '' === $phone ) {
			$phone = $order->get_meta( self::get_order_meta_key( 'ORDER_META_CUSTOMER_PHONE', '_gaticrew_customer_phone' ), true );
		}

		return array(
			'name'  => '' !== $name ? sanitize_text_field( $name ) : __( 'Guest', 'gaticrew-events-bridge' ),
			'email' => sanitize_email( $email ),
			'phone' => function_exists( 'wc_sanitize_phone_number' ) ? wc_sanitize_phone_number( $phone ) : sanitize_text_field( $phone ),
		);
	}

	/**
	 * Finds a linked event by Woo product ID.
	 *
	 * @param int $product_id Woo product ID.
	 * @return int
	 */
	private static function get_event_id_by_product_id( $product_id ) {
		$product_id = absint( $product_id );

		if ( ! $product_id ) {
			return 0;
		}

		$events = get_posts(
			array(
				'post_type'              => class_exists( 'GatiCrew_Events_Bridge_Events' ) ? GatiCrew_Events_Bridge_Events::get_event_post_type() : 'tribe_events',
				'post_status'            => 'publish',
				'posts_per_page'         => 1,
				'fields'                 => 'ids',
				'no_found_rows'          => true,
				'update_post_meta_cache' => false,
				'update_post_term_cache' => false,
				'meta_query'             => array(
					'relation' => 'OR',
					array(
						'key'     => '_gaticrew_product_id',
						'value'   => $product_id,
						'compare' => '=',
						'type'    => 'NUMERIC',
					),
					array(
						'key'     => self::get_event_product_meta_key(),
						'value'   => $product_id,
						'compare' => '=',
						'type'    => 'NUMERIC',
					),
				),
			)
		);

		return ! empty( $events[0] ) ? absint( $events[0] ) : 0;
	}

	/**
	 * Returns product ID from an order line.
	 *
	 * @param WC_Order_Item_Product $item Order item.
	 * @return int
	 */
	private static function get_item_product_id( WC_Order_Item_Product $item ) {
		return absint( $item->get_variation_id() ? $item->get_variation_id() : $item->get_product_id() );
	}

	/**
	 * Finds the first line item ID matching the linked product.
	 *
	 * @param WC_Order $order Woo order.
	 * @param int      $product_id Woo product ID.
	 * @return int
	 */
	private static function get_first_matching_order_item_id( WC_Order $order, $product_id ) {
		foreach ( $order->get_items( 'line_item' ) as $item_id => $item ) {
			if ( $item instanceof WC_Order_Item_Product && absint( $product_id ) === self::get_item_product_id( $item ) ) {
				return absint( $item_id );
			}
		}

		return 0;
	}

	/**
	 * Returns the event/product meta key used by this plugin.
	 *
	 * @return string
	 */
	private static function get_event_product_meta_key() {
		return class_exists( 'GatiCrew_Events_Bridge' ) && defined( 'GatiCrew_Events_Bridge::META_KEY_TICKET_PRODUCT_ID' )
			? GatiCrew_Events_Bridge::META_KEY_TICKET_PRODUCT_ID
			: '_gaticrew_ticket_product_id';
	}

	/**
	 * Returns an order meta key with fallback for early REST contexts.
	 *
	 * @param string $constant Class constant name.
	 * @param string $fallback Fallback meta key.
	 * @return string
	 */
	private static function get_order_meta_key( $constant, $fallback ) {
		return class_exists( 'GatiCrew_Events_Bridge' ) && defined( 'GatiCrew_Events_Bridge::' . $constant )
			? constant( 'GatiCrew_Events_Bridge::' . $constant )
			: $fallback;
	}

	/**
	 * Writes sync diagnostics to the PHP error log.
	 *
	 * @param string $message Message.
	 * @return void
	 */
	private static function debug_log( $message ) {
		error_log( '[GatiCrew Event Tickets Sync] ' . (string) $message ); // phpcs:ignore WordPress.PHP.DevelopmentFunctions.error_log_error_log -- Temporary diagnostics for attendee sync.
	}

